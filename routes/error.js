/*!
 * =============================================================================
 * Router for 404 errors
 * =============================================================================
 */

// Import packages.
const router = require('express').Router();

/*!
 * =============================================================================
 * GET
 * =============================================================================
 */

router.get('/', (req, res, next) => {
  res.render('error', {
    javascript: undefined,
    stylesheet: '/stylesheets/error.css'
  });
});

/*!
 * =============================================================================
 * Export router
 * =============================================================================
 */

module.exports = router;