/*!
 * =============================================================================
 * Router for /logout
 * =============================================================================
 */

// Import packages.
const router = require('express').Router();

/*!
 * =============================================================================
 * GET
 * =============================================================================
 */

router.get('/', (req, res, next) => {
  res.redirect('/login');
});

/*!
 * =============================================================================
 * POST
 * =============================================================================
 */

router.post('/', (req, res) => {
  req.logout((err) => {
    if (err) {
      return next(err);
    }

    res.redirect('/login');
  });
});

/*!
 * =============================================================================
 * Export router
 * =============================================================================
 */

module.exports = router;
